#!/usr/bin/env python3
"""Synchronize benchmark metadata and screenshots from model directories."""

from __future__ import annotations

import argparse
import json
import re
import shutil
import subprocess
import tempfile
import unicodedata
from datetime import date
from html.parser import HTMLParser
from pathlib import Path


ROOT = Path(__file__).resolve().parent
JSON_PATH = ROOT / "benchmarks.json"
JS_PATH = ROOT / "benchmarks-data.js"
SCREENSHOT_DIR = ROOT / "screenshots"


class TitleParser(HTMLParser):
    def __init__(self) -> None:
        super().__init__()
        self.in_title = False
        self.parts: list[str] = []

    def handle_starttag(self, tag: str, attrs: list[tuple[str, str | None]]) -> None:
        if tag.lower() == "title":
            self.in_title = True

    def handle_endtag(self, tag: str) -> None:
        if tag.lower() == "title":
            self.in_title = False

    def handle_data(self, data: str) -> None:
        if self.in_title:
            self.parts.append(data)

    @property
    def title(self) -> str:
        return " ".join("".join(self.parts).split())


def slugify(value: str) -> str:
    normalized = unicodedata.normalize("NFKD", value)
    ascii_value = normalized.encode("ascii", "ignore").decode("ascii").lower()
    slug = re.sub(r"[^a-z0-9]+", "-", ascii_value).strip("-")
    return slug or "model"


def read_title(index_path: Path) -> str:
    parser = TitleParser()
    parser.feed(index_path.read_text(encoding="utf-8", errors="replace"))
    return parser.title or index_path.parent.name


def load_existing() -> dict:
    if not JSON_PATH.exists():
        return {"benchmark": "2DAction.bench", "prompt": "prompt.md", "results": []}
    try:
        return json.loads(JSON_PATH.read_text(encoding="utf-8"))
    except (json.JSONDecodeError, OSError) as exc:
        raise SystemExit(f"Cannot read {JSON_PATH.name}: {exc}") from exc


def discover_model_dirs() -> list[Path]:
    ignored = {"screenshots", ".git", ".openai", "__pycache__"}
    return sorted(
        (
            path
            for path in ROOT.iterdir()
            if path.is_dir()
            and path.name not in ignored
            and (path / "index.html").is_file()
            and (path / "game.js").is_file()
        ),
        key=lambda path: path.name.casefold(),
    )


def unique_ids(model_dirs: list[Path]) -> dict[Path, str]:
    result: dict[Path, str] = {}
    used: set[str] = set()
    for model_dir in model_dirs:
        base = slugify(model_dir.name)
        candidate = base
        suffix = 2
        while candidate in used:
            candidate = f"{base}-{suffix}"
            suffix += 1
        used.add(candidate)
        result[model_dir] = candidate
    return result


def capture_screenshot(index_path: Path, destination: Path) -> bool:
    qlmanage = shutil.which("qlmanage")
    if not qlmanage:
        print(f"  warning: qlmanage is unavailable; skipped {destination.name}")
        return False

    with tempfile.TemporaryDirectory(prefix="2daction-shot-") as temp_dir:
        command = [qlmanage, "-t", "-s", "1200", "-o", temp_dir, str(index_path)]
        completed = subprocess.run(command, capture_output=True, text=True, check=False)
        generated = Path(temp_dir) / "index.html.png"
        if completed.returncode != 0 or not generated.exists():
            print(f"  warning: could not capture {index_path.parent.name}")
            return False
        destination.parent.mkdir(parents=True, exist_ok=True)
        shutil.move(str(generated), destination)
        return True


def write_outputs(data: dict) -> None:
    serialized = json.dumps(data, ensure_ascii=False, indent=2) + "\n"
    JSON_PATH.write_text(serialized, encoding="utf-8")
    JS_PATH.write_text(
        "// Generated by sync_benchmarks.py. Do not edit manually.\n"
        f"window.BENCHMARK_DATA = {serialized.rstrip()};\n",
        encoding="utf-8",
    )


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--refresh-screenshots",
        action="store_true",
        help="regenerate every screenshot instead of only missing ones",
    )
    parser.add_argument(
        "--no-screenshots",
        action="store_true",
        help="update metadata without generating screenshots",
    )
    args = parser.parse_args()

    existing = load_existing()
    existing_by_model = {item.get("model"): item for item in existing.get("results", [])}
    existing_by_id = {item.get("id"): item for item in existing.get("results", [])}
    model_dirs = discover_model_dirs()
    ids = unique_ids(model_dirs)
    results = []

    print(f"Found {len(model_dirs)} model directories")
    for model_dir in model_dirs:
        model = model_dir.name
        item_id = ids[model_dir]
        previous = existing_by_model.get(model) or existing_by_id.get(item_id) or {}
        screenshot = f"screenshots/{item_id}.png"
        screenshot_path = ROOT / screenshot

        if not args.no_screenshots and (args.refresh_screenshots or not screenshot_path.exists()):
            action = "refreshed" if screenshot_path.exists() else "created"
            if capture_screenshot(model_dir / "index.html", screenshot_path):
                print(f"  {action}: {screenshot}")

        results.append(
            {
                "id": item_id,
                "model": model,
                "gameTitle": read_title(model_dir / "index.html"),
                "status": previous.get("status", "untested"),
                "notes": previous.get("notes", ""),
                "screenshot": screenshot,
                "gameUrl": f"{model}/index.html",
            }
        )

    data = {
        "benchmark": existing.get("benchmark", "2DAction.bench"),
        "prompt": existing.get("prompt", "prompt.md"),
        "updatedAt": date.today().isoformat(),
        "results": results,
    }
    write_outputs(data)
    print(f"Updated {JSON_PATH.name} and {JS_PATH.name}")


if __name__ == "__main__":
    main()
